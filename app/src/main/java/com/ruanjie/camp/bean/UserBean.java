    public static void saveLogin(UserBean userBean) {
        if (userBean == null) {
            return;
        }

        SP.setIsLogin(true);
        if (!TextUtils.isEmpty(userBean.user_id)) {
            SP.seteUserID(userBean.user_id);
        }
//        if (!TextUtils.isEmpty(userBean.im_username)) {
//            SP.setImUserName(userBean.im_username);
//        }
//        if (!TextUtils.isEmpty(userBean.im_password)) {
//            SP.setImPassword(userBean.im_password);
//        }
        if (!TextUtils.isEmpty(userBean.phone)) {
            SP.setMobile(userBean.phone);
        }

        if (!TextUtils.isEmpty(userBean.email)) {
            SP.setEmail(userBean.email);
        }

        if (!TextUtils.isEmpty(userBean.user_name)) {
            SP.setUserName(userBean.user_name);
        }
//        if (!TextUtils.isEmpty(userBean.real_name)) {
//            SP.setRealName(userBean.real_name);
//        }
        if (!TextUtils.isEmpty(userBean.nickname)) {
            SP.setUserNickname(userBean.nickname);
        }
        if (!TextUtils.isEmpty(userBean.head)) {
            SP.setHeadImg(userBean.head);
        }
        if (!TextUtils.isEmpty(userBean.token)) {
            SP.setToken(userBean.token);
        }
        if (!TextUtils.isEmpty(userBean.score)) {
            SP.setScore(userBean.score);
        }

        SP.setDriverCheckStatus(userBean.driver_check_status);

        SP.setLevel(userBean.level_rank);
        SP.setLevelVip(userBean.vip_level);
        SP.setVipEndTime(userBean.vip_end_time);

        SP.setIsBindWechat(userBean.is_bind_wechat);
        SP.setIsBindQq(userBean.is_bind_qq);

        if (!TextUtils.isEmpty(userBean.vip_name)) {
            SP.setLevelVipName(userBean.vip_name);
        }

        SP.setIsFirstEntry(true);
    }

